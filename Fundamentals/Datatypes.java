

public class Datatypes {

    public static void main(String[] args)
    {

    

    //primitive data types 
   // Integer datatypes byte , short , int , long  follows base 2 format
    byte age = 120;      // 1 byte
  short salary_of_fresher = 3300; //2 byte
  int phone_no = 123456789;      // 4 byte
  long population_of_country = 123456789000l;  // 8 byte
  
  //real no. --> floating types  follows ieee- 754 single precision format
  float a = 24.17f;                 // 4 bytes
   double b = 9999.13;

   char var = 'h'; 

   System.out.println(age);
   System.out.println(salary_of_fresher);
   System.out.println(phone_no);
   System.out.println(population_of_country);

   System.out.println(a);
   System.out.println(b);
   System.out.println(var);
    }
}
